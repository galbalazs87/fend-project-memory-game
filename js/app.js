let card = document.getElementsByClassName("card");
let cards = [...card];

// Declaring the card deck
const deck = document.getElementById("card-deck");

// Declaring the move variable to be able to count
let moves = 0;
let counter = document.querySelector(".moves");

// Declaring variable stars for the star-counting
const stars = document.querySelectorAll(".fa-star");

// Declaring variable for the cards that match
let matchedCard = document.getElementsByClassName("match");

 // Declaring variable for the list of stars
 let starsList = document.querySelectorAll(".stars li");

 // Declaring the modal which pops up when the game is over
 let modal = document.getElementById("modal");

  // Declaring the variable for the icon which can close the modal
  let closeicon = document.querySelector(".close");

 // Declaring the array for the currently opened cards
var openedCards = [];


